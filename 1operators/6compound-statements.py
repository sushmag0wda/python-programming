#compound statements
a=10
b=20
#addition
a+=b #a=a+b
print(a) #10+20=30

#subtraction
b-=a #b=b-a
print(b) #20-30=-10

a=2
b=3
#multiplication
a*=b #a=a*b
print(a)# 2*3=6 

#exponentiation
a**=b #a=a**b
print(a) #2**3=8

a=10
b=6
#floating point division
b/=a #b=b/a
print(b) #6/10=0.6

#floor division
a//=b #a=a//b
print(a) #10//6=1   

#modulus   
b%=a #b=b%a
print(b) #10%6=4