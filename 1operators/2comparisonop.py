# Comparison /relational Operators
print(10 < 12)#True because 10 is less than 12
print(5 > 6)#False because 5 is not greater than 6
print(22.67 >= 10.67778888)# True because 22.67 is greater than or equal to 10.67778888 
print((10*3) <= (15**2))#True because 30 is less than or equal to 225
print(True == True)#True because both are True
print(False != (2**0))#False because 2**0 is 1 and False is not equal to 1
print("ap" == "aa")#False because "ap" is not equal to "aa"
print(97 == "97")#False because 97 is an integer and "97" is a string