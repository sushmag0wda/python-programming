# Arithmetic Operations
#addition
print(2+3)#addition
print(3+4+5.6+7)#addition
print(+4)#represents +ve value
print("-----------------------------------------------")
#subtraction
print(5-3)#subtraction
print(100-3.4-6.7-2.0)#subtraction
print(-4)#represents -ve value
print("-----------------------------------------------")
#multiplication
print(5*3)#multiplication
print(100*3.4*6.7*2.0)#multiplication
#print(*4)#type error
print('8'*4)#8888
print("-----------------------------------------------")
#exponentiation
print(2 ** 3)#2 power 3(2*2*2)
print(3 ** 2)#3 power 2(3*3)
print("-----------------------------------------------")
#floor division
print(11 // 2) # floor division (gives quotient)
print(22 // 7) # floor division (gives quotient)
print(100 // 3.4) # floor division (gives quotient)
print("-----------------------------------------------")
#floating point division
print(11 / 2) # 5.5 #floating point division
print(22 / 7) # 3.142857142857143 # floating point division
print("-----------------------------------------------")
#modulus
print(11 % 2) # 1 
print(22 % 7) # 1
print(5%25) # 5
print("-----------------------------------------------")
